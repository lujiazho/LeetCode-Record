///////////////////////////////////////////////////////////////////////////////////////////////
// easy
////////////////
// Time Complexity: O(number of bits)
// Space Complexity: O(1)
///////////////////////////////////////////////////////////////////////////////////////////////
class Solution {
public:
    int hammingWeight(uint32_t n) {
        int res = 0;
        while (n){
            if (n % 2 == 1)
                res += 1;
            n = n >> 1;
        }
        return res;
    }
};