///////////////////////////////////////////////////////////////////////////////////////////////
// easy
////////////////
// Time Complexity: O(n)
// Space Complexity: O(1)
///////////////////////////////////////////////////////////////////////////////////////////////
class Solution {
public:
    int numberOfSteps(int num) {
        int step = 0;
        while (num){
            if (num%2) num--;
            else num/=2;
            step++;
        }
        return step;
    }
};