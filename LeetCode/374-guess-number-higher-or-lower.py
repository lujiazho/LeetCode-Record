# easy
###########
# 时间复杂度：O(logn)
# 空间复杂度：O(1)
###############################################################################################
class Solution:
    def guessNumber(self, n: int) -> int:
        l, r, g = 1, n, (1+n)>>1
        while l < r:
            g = (l+r+1)>>1
            if guess(g) >= 0:
                l = g
            else:
                r = g - 1
        return l